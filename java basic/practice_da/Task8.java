public class Task8 {
    public static void main(String[] args) {
        float a = 7;
        float b = 2;
        float div = a / b;

        System.out.printf("Division result: %.2f%n", div);

        int x = 7, y = 2;
        int quotient = x / y;
        int remainder = x % y;

        System.out.println("Quotient: " + quotient);
        System.out.println("Remainder: " + remainder);
    }
}
